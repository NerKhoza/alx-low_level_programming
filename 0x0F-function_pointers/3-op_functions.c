#include "3-cal.h"
#include <stdio.h>
#include <stdlib.h>


/**
 * op_add - function that adds two numbers
 * @a: parameter 1
 * @b: parameter 2
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that subtracts two numbers
 * @a: parameter 1
 * @b: parameter 2
 * Return: a + b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that finds the product of two numbers
 * @a: parameter 1
 * @b: parameter 2
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function for the quotient of two numbers
 * @a: parameter 1
 * @b: parameter 2
 * Return: quotient of two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo function
 * @a: parameter 1
 * @b: parameter 2
 * Return: modulo of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}


